function toggleAudio() {
  var audio = document.getElementById('audio');
  var button = document.getElementById('button');

  if (audio.paused) {
    audio.play();
    button.innerHTML = 'Pause';
  } else {
    audio.pause();
    button.innerHTML = 'Play';
  }
}

